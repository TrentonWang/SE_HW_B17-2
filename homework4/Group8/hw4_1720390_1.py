#   编写程序，计算百鸡百钱的问题
#   假设公鸡5元一只，母鸡3元一只，小鸡一元三只
#   现在有100块钱，问有多少种买法？
# a为公鸡 b为母鸡 100-a-b为小鸡的数量 count为购买方法次数
count = 0
a = 0
b = 0
print("一百块买100只鸡有如下买法：")
for a in range(int(100/5)):
    for b in  range(int(100/3)):
        if(5*a+3*b+(100-a-b)/3==100):
            print("公鸡有"+str(a)+"只"+" 母鸡有"+str(b)+"只"+" 小鸡有"+str((100-a-b))+"只")
            count=count+1
print("一共有"+str(count)+"种买法")

#   公鸡有0只 母鸡有25只 小鸡有75只
#   公鸡有4只 母鸡有18只 小鸡有78只
#   公鸡有8只 母鸡有11只 小鸡有81只
#   公鸡有12只 母鸡有4只 小鸡有84只
#   一共有4种买法
