# 输入 姓名、性别、年龄 ，将数据存储为一个字典
S1 = input("请输入姓名：")
S2 = input("请输入性别：")
S3 = input("请输入年龄：")
SDict = {'姓名' : S1 , '性别' : S2 , '年龄' : S3}
# print(SDict)

# 输出个人介绍——我的名字XXX，今年XXX岁，性别XX，喜欢敲代码
