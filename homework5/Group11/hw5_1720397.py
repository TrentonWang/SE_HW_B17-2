# 提醒用户输入身高、联系方式，把数据添加到1中创建的字典
S4 = input("请输入身高：")
S5 = input("请输入联系方式：")
SDict['身高'] =  S4
SDict['联系方式'] = S5

# 循环输出完成后的字典中的所有信息
for Key , Value in SDict.items():
    print(Key + "：" + Value)

# 当前有一个列表 li = [11,22,33,22,22,44,55,77,88,99,11]，请去除重复元素之后，再统计元素的数量
li = [11,22,33,22,22,44,55,77,88,99,11]
S6 = set(li)
li = list(S6)
S6 = dict()
for s in li:
    S6[s] = S6.get(s, 0) + 1
print(S6)

